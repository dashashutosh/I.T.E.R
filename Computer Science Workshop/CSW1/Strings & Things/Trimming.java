package Strings_N_Things;

public class Trimming {

	public static void main(String[] args) {
		
		String s1="		Examples of trim method";
		
		System.out.println("without trimming output is: "+s1);
		System.out.println("trimming output is: "+s1.trim()+"HELLO TRIM");
		System.out.println(s1.trim());
		
	}

}
