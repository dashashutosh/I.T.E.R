package Chapter_5;

public class ShiftOperators {

	public static void main(String[] args) {
		
		int a=8,b=-10;
		System.out.println("8>>>2: "+ (a>>>2));
		System.out.println("8>>2: "+ (a>>2));
		System.out.println("8<<1: "+ (a<<1));
		System.out.println("8<<2: "+ (a<<2));
		
		System.out.println("-10>>1: "+ (b>>1));
		System.out.println("-10>>>1: "+ (b>>>1));
		System.out.println("-10<<1: "+ (b<<1));
		System.out.println("-10<<2: "+ (b<<2));
		
	}

}
